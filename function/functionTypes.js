/* 
Types of function
1. No parameters, no return;
2. No parameters, return;
3. Parameters, no return;
4. Parameters, return;
*/

// 1. No parameters, no return;
function fun1() {
    console.log("No parameters, no return;")
}
// 2. No parameters, return;
function fun2() {
    return 9;
}
// 3. Parameters, no return;
function fun3(name){
    console.log(name);
}
// 4. Parameters, return;
function fun4(x, y) {
    return x + y;
}